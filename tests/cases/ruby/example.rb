# tests/cases/ruby/example.rb

# Example Ruby script demonstrating while, if, and for statements

# While loop
i = 0
while i < 5
  puts "While loop iteration: #{i}"
  i += 1
end

# If statement
number = 10
if number > 5
  puts "Number is greater than 5"
else
  puts "Number is 5 or less"
end

# For loop
for j in 1..3
  puts "For loop iteration: #{j}"
end